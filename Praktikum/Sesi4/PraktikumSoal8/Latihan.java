public class Latihan
	{
	public static void main(String args[])
		{
		int count = 1; // mendeklarasikan variable count dengan jenis data integer dan bernilai 1 
		while(count < 5) // diberi operasi while apabila nilai integer kurang dari 5 maka operasi akan terus berjalan
			{
			System.out.println("Count is equal to " + count); //menampilkan "count is equal to ( nilai dari variabel count )
			count++; // memberi operasi increment pada variabel count
			}
		}
	}
