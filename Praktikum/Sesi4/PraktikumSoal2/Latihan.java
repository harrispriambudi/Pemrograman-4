public class Latihan
	{
	public static void main(String args[])
		{
		for (int x = 0; x < 7; x++)
			{
			System.out.println("Hello, Worlds we are usin loops");
			}
		}
	}
/* keterangan = parameter pertama diisi dengan int x = 0, ini artinya adalah kita
akan menggunakan variabel x sebagai counter (penghitung) untuk for diawali dari 0. Parameter
kedua diisi dengan x < 7, artinya iterasi for akan terus belangsung selama i bernilai kurang
dari x dan berhenti ketika x sudah mencapai nilai tujuh. Parameter ketiga diisi dengan i++
artinya setiap kali iterasi maka counter i akan naik satu nilai. Ketiga parameter ini
mengindikasikan bahwa iterasi dilakukan dari i bernilai 0, berakhir ketika i mencapai nilai 10
dan setiap kali iterasi i naik satu-satu, total akan terjadi 7x perulangan. */

