public class Latihan
	{
	public static void main(String[] args)
		{
		int a=4, b=6; //deklarasi varaibel a bernilai 4 dan berjenis integer, deklarasi varaibel b bernilai 6 dan berjenis integer
		int num=a|b; // deklarasi variabel num dan diberi operator or
		if(num==4) // memberi kondisi apabila variabel num bernilai 4 akan menampilkan "value is 4"
			System.out.println("value is 4");
		else if(num==5) // menampilkan display "value is 5" apabila variabel num bernilai 5
			System.out.println("value is 5");
		else if(num==6) //menampilkan display "value is 6" apabila variabel num bernilai 6
			System.out.println("value is 6");
		else if(num==7) //menampilkan display "value is 7" apabila variabel num bernilai 7
			System.out.println("value is 7");
		}
	}
